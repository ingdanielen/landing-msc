export type AdminTranslations = typeof adminTranslations.es

// Helper function to get category label by key
export function getCategoryLabelByKey(key: string, lang: AdminLang): string {
  const categories = adminTranslations[lang].categories
  const categoryMap: Record<string, keyof typeof categories> = {
    "Noticias": "news",
    "Tecnico": "technical",
    "Normativas": "regulations",
    "Empresa": "company",
    "Educativo": "educational",
  }
  return categories[categoryMap[key] || "news"] || key
}
